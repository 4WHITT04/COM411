#user input
print("i've found a robot who's trapped under some cables! How many should i move to get them free?")

cables = int(input())
#iterating variable set to 0.
cablegone = 0
print()

#while loop
while (cablegone < cables):

  print("Removing cables!")
  cablegone = cablegone + 1

#indication of condition fulfillment.
print()
print("There sure are a lot of cables here!")