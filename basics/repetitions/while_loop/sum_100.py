#initial commment
print("Now calculating the sum of numers 1 to 100")

#control variable
startingno = 1

#variable for calculated sum
sumno = 0
print()

#while loop for calculation.
while(startingno <= 100):
  
  sumno = sumno + startingno
  
  startingno = startingno + 1 


print("Calculations complete! The total is:", sumno, "Thats a huge number!")
