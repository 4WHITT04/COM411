#code to show modulo operators.
print("Give me a whole number, please!")
wholenum = int(input())

#if statement using modulo to differentiate odd and even numbers.
if (wholenum % 2 != 0):
  print("That number is an odd number, i'm sure of it!")
else:
  print("Thats an even number. Perfectly balanced, as all things should be...")
